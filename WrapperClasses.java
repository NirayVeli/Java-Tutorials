
public class WrapperClasses {

	@SuppressWarnings("unused")
	public static void main(String[] args) {
		
		// Primitive  -----  Wrapper
		
		// boolean    -----  Boolean
		// int        -----  Integer
		// double     -----  Double
		// char       -----  Character
		
		
			Boolean x = true;
			Integer y = 357;
			Double z = 32.66;
			Character a = '$';
			
			
		// Also work as primitive types
			
			if(x = true) {
				System.out.println("This is true");
			}
			
	}

}
